"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { signOut } from "next-auth/react";

const navItems = [
  { href: "/photos", label: "Fotos", icon: PhotoIcon },
  { href: "/collections", label: "Colecciones", icon: FolderIcon },
  { href: "/upload", label: "Subir", icon: UploadIcon },
  { href: "/settings", label: "Ajustes", icon: SettingsIcon },
];

interface DashboardShellProps {
  user: { name?: string | null; image?: string | null; email?: string | null };
  children: React.ReactNode;
}

export function DashboardShell({ user, children }: DashboardShellProps) {
  const pathname = usePathname();

  return (
    <div className="flex min-h-screen flex-col bg-neutral-950 text-white">
      {/* Top bar (desktop) */}
      <header className="hidden border-b border-neutral-800 md:block">
        <div className="mx-auto flex h-14 max-w-7xl items-center justify-between px-4">
          <Link href="/photos" className="text-lg font-bold tracking-tight">
            Chikiluki Gallery
          </Link>
          <nav className="flex items-center gap-1">
            {navItems.map((item) => (
              <Link
                key={item.href}
                href={item.href}
                className={`rounded-md px-3 py-2 text-sm transition ${
                  pathname.startsWith(item.href)
                    ? "bg-neutral-800 text-white"
                    : "text-neutral-400 hover:text-white"
                }`}
              >
                {item.label}
              </Link>
            ))}
          </nav>
          <button
            onClick={() => signOut({ callbackUrl: "/login" })}
            className="flex items-center gap-2 rounded-md px-3 py-2 text-sm text-neutral-400 transition hover:text-white"
          >
            {user.image && (
              <img
                src={user.image}
                alt=""
                className="h-6 w-6 rounded-full"
              />
            )}
            Salir
          </button>
        </div>
      </header>

      {/* Main content */}
      <main className="mx-auto w-full max-w-7xl flex-1 px-4 pb-20 pt-4 md:pb-4">
        {children}
      </main>

      {/* Bottom nav (mobile) */}
      <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-neutral-800 bg-neutral-950/95 backdrop-blur-sm md:hidden">
        <div className="flex items-center justify-around py-2">
          {navItems.map((item) => {
            const isActive = pathname.startsWith(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex flex-col items-center gap-0.5 px-3 py-1 text-xs transition ${
                  isActive ? "text-white" : "text-neutral-500"
                }`}
              >
                <item.icon
                  className={`h-5 w-5 ${isActive ? "text-white" : "text-neutral-500"}`}
                />
                {item.label}
              </Link>
            );
          })}
        </div>
      </nav>
    </div>
  );
}

